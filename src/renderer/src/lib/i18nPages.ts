export function pageText(locale: Locale, page: string, key: string, params?: Params): string {
  let s = pages[locale]?.[page]?.[key] ?? pages.it[page]?.[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.split(`{${k}}`).join(String(v));
    }
  }
  return s;
}
